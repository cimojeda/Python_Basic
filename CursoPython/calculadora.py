def suma(a,b):
    suma=a+b
    return print(f'la suma de {a} + {b} es: ',suma)

def resta(a,b):
    resta=a-b
    return print(f'la resta de {a} - {b} es: ',resta)

def multiplica(a,b):
    multiplica=a*b
    return print(f'la multiplicación de {a} * {b} es: ',multiplica)

def division(a,b):
    division=a/b
    return print(f'la división de {a} / {b} es: ',division)
